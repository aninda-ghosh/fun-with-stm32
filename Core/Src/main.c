/* Includes ------------------------------------------------------------------*/
#include "stdio.h"
#include "string.h"
#include "main.h"


UART_HandleTypeDef huart1;


#define SIMULATOR_FREQUENCY 100	//(Frequency in Hertz)
uint16_t signal_toggle = 0;     //Will be used to get the Toggle count
uint16_t ms_counter = 0;		//Will be used to calculate the time passed
uint16_t prev_ms_counter = 0;   //Will be used in simulation


/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_USART1_UART_Init(void);

/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void) {
	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* Configure the system clock */
	SystemClock_Config();

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_USART1_UART_Init();

	/* Infinite loop */
	while (1) {

		//This is the Signal Simulator code
		if(ms_counter != prev_ms_counter)
		{
			if((ms_counter - prev_ms_counter) % (1000/SIMULATOR_FREQUENCY) == 0)
			{
				HAL_GPIO_TogglePin(Sensor_Simulator_GPIO_Port, Sensor_Simulator_Pin);
				prev_ms_counter = ms_counter;
			}
		}


		//Here I will decode the Signal Frequency and Print it
		//ASSUMTION Min Frequency that can be measured here -> (1/30)Hz, Max Frequency that can be measured here -> 1000Hz
		if(ms_counter >= 30000) //30 Seconds
		{
			char textdata[20] = {0};
			sprintf(textdata, "Freq -> [%0.2f]\n", ((float)signal_toggle/30));
			HAL_UART_Transmit(&huart1, (uint8_t *)textdata, strlen(textdata), 1000);
			signal_toggle = 0;	//Reset the toggle counter
			prev_ms_counter = ms_counter = 0;
		}
	}
}

